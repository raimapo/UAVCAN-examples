/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */

	uint32_t value = 1000000;

	can.init(value);

	getNode().setName("org.test.CustomDataType");
	getNode().setNodeID(5);

	using custom_data_types::PerformLinearLeastSquaresFit;
    uavcan::ServiceServer<PerformLinearLeastSquaresFit> srv_least_squares(getNode());

    auto regist_result =
        uavcan::GlobalDataTypeRegistry::instance().registerDataType<PerformLinearLeastSquaresFit>(243); // DTID = 243
    if (regist_result != uavcan::GlobalDataTypeRegistry::RegistrationResultOk)
    {
    	DEBUG_Printf("Failed to register the data type 1");
    }


    const int res = srv_least_squares.start(
    	[](const PerformLinearLeastSquaresFit::Request& request, PerformLinearLeastSquaresFit::Response& response)
		{
    		double sum_x = 0, sum_y = 0, sum_xy = 0, sum_xx = 0;
    		for (auto point : request.points)
    	    {
    			sum_x += point.x;
    	        sum_y += point.y;
    	        sum_xy += point.x * point.y;
    	        sum_xx += point.x * point.x;
    	    }
            const double a = sum_x * sum_y - request.points.size() * sum_xy;
            const double b = sum_x * sum_x - request.points.size() * sum_xx;
            if (abs(b) > 1e-12)
            {
                response.slope = a / b;
                response.y_intercept = (sum_y - response.slope * sum_x) / request.points.size();
            }

    		response.MyNumber = 256.0;

        });
    if (res < 0)
    {
    	DEBUG_Printf("Failed to start the PerformLinearLeastSquaresFit server\r\n");
    }



	if (getNode().start() < 0) {
		DEBUG_Printf("UAVCAN start fail\r\n");
	    while (1);
	}

	getNode().setModeOperational();

  /* Infinite loop */
  for(;;)
  {
	    const int res = getNode().spin(uavcan::MonotonicDuration::fromMSec(500));
	    if (res < 0) {
	    	DEBUG_Printf("UAVCAN spin fail\r\n");
	    	while(1);
	    }
  }
  /* USER CODE END 5 */ 
}

